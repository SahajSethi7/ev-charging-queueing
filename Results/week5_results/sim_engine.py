# Default per-type fault rates.
# Source: Jiaxing dataset EDA (Week 2), computed as fraction of sessions
# with is_abnormal=1 within each charger_type group:
#   DC_Fast: 9,451 / 135,359 = 6.98% ~ 7%
#   Level_2: 40,730 / 131,329 = 31.02% ~ 31%
#   Mixed:   6,577 / 82,221  = 8.00% ~ 8%
# These are dataset-level averages; per-station rates vary.
DEFAULT_FAULT_RATES = {
    'DC_Fast': 0.07,
    'Level_2': 0.31,
    'Mixed': 0.08,
}


def is_session_faulted(charger_type: str, fault_rates: Optional[dict],
                       rng: np.random.Generator) -> bool:
    """
    Determine if a session is faulted (session-start Bernoulli).

    Mid-session failure modeling is deferred unless empirical records
    clearly require it.
    """
    rates = fault_rates or DEFAULT_FAULT_RATES
    rate = rates.get(charger_type, 0.186)  # global fallback
    return rng.random() < rate


def fault_repair_duration(rng: np.random.Generator,
                          median_min: float = 30.0) -> float:
    """
    Duration (minutes) a CHARGER is blocked after a fault event.

    This represents repair/reset downtime, NOT the logged session
    duration from the dataset (~1.3 min median). The dataset records
    how long the customer's failed session lasted, but does not record
    how long the charger was unavailable afterward. Using the session
    duration (~1 min) as the fault occupancy causes faults to act as
    a capacity BOOST (short faults replace long normal sessions),
    which is the opposite of the real-world effect.

    The repair duration is drawn from a lognormal distribution with
    configurable median. Default median = 30 minutes, σ = 0.8, giving:
      - ~80% of repairs between 10 and 90 minutes
      - mean ≈ 41 minutes
      - 95th percentile ≈ 105 minutes
    This is a modeling assumption, not a data-derived parameter.
    Sensitivity analysis should vary the median (e.g., 15, 30, 60 min)
    to test robustness of fleet-sizing conclusions.

    The floor is 5 minutes (even a quick software reset takes time).
    """
    mu = np.log(median_min)  # lognormal mu = log(median)
    sigma = 0.8
    return max(5.0, float(rng.lognormal(mu, sigma)))


