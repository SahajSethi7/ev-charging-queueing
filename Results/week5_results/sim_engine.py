@dataclass
class MetricsCollector:
    """Collects simulation metrics.

    capacity_event_log records (timestamp, scheduled_capacity, n_busy)
    at every state-change event: capacity transitions, service starts,
    and service ends. This allows exact computation of the realized
    active-server-minutes integral for the utilization denominator
    under demand-responsive activation.
    """
    sessions: List[SessionRecord] = field(default_factory=list)
    charger_busy_time: Optional[np.ndarray] = None
    charger_fault_time: Optional[np.ndarray] = None
    sim_duration_min: float = 0.0
    # Event-driven capacity log: list of (time, scheduled_cap, n_busy)
    capacity_event_log: List = field(default_factory=list)
    busy_intervals: List = field(default_factory=list)

    def add_session(self, record: SessionRecord):
        self.sessions.append(record)

    def add_busy_interval(self, start: float, end: float):
        """Record charger occupancy for a service or repair attempt."""
        self.busy_intervals.append((float(start), float(end)))

    def log_capacity_event(self, time: float, scheduled_cap: int,
                           n_busy: int):
        """Record a state-change event for realized-capacity tracking.

        Called at: capacity transitions, service starts, service ends.
        Multiple events at the same timestamp are allowed; the integral
        computation deduplicates by keeping the last state at each time.
        """
        self.capacity_event_log.append((time, scheduled_cap, n_busy))

    def compute_realized_capacity_minutes(self, measurement_start_min: float,
                                          measurement_end_min: float) -> float:
        """Compute active-server-minutes over the measurement window."""
        if not self.capacity_event_log:
            return 0.0

        sorted_log = sorted(self.capacity_event_log, key=lambda x: x[0])
        deduped = []
        for i, entry in enumerate(sorted_log):
            if i + 1 < len(sorted_log) and sorted_log[i + 1][0] == entry[0]:
                continue
            deduped.append(entry)

        integral = 0.0
        for i, (t, sched_cap, n_busy) in enumerate(deduped):
            t_start = max(t, measurement_start_min)
            if t_start >= measurement_end_min:
                break
            if i + 1 < len(deduped):
                t_end = min(deduped[i + 1][0], measurement_end_min)
            else:
                t_end = measurement_end_min
            t_end = min(t_end, measurement_end_min)
            if t_end > t_start:
                realized_cap = max(sched_cap, n_busy)
                integral += realized_cap * (t_end - t_start)

        return integral

    def _compute_capacity_metrics(self, n_chargers: int,
                                  measurement_start_min: float,
                                  measurement_duration_min: float,
                                  activation_schedule: Optional[np.ndarray]
                                  ) -> dict:
        """Compute capacity and scheduling metrics independently of sessions."""
        measurement_end_min = measurement_start_min + measurement_duration_min
        static_capacity = n_chargers * measurement_duration_min

        realized_capacity = self.compute_realized_capacity_minutes(
            measurement_start_min, measurement_end_min)
        if realized_capacity <= 0:
            realized_capacity = static_capacity

        if activation_schedule is not None:
            scheduled_minutes = 0.0
            first_hour = int(np.floor(measurement_start_min / 60.0))
            last_hour = int(np.ceil(measurement_end_min / 60.0))
            for h_idx in range(first_hour, last_hour):
                hour_of_day = h_idx % 24
                hour_start = max(h_idx * 60.0, measurement_start_min)
                hour_end = min((h_idx + 1) * 60.0, measurement_end_min)
                duration_in_hour = hour_end - hour_start
                scheduled_minutes += (
                    activation_schedule[hour_of_day] * duration_in_hour
                )
            scheduled_ch = scheduled_minutes / 60.0
        else:
            scheduled_ch = n_chargers * (measurement_duration_min / 60.0)

        return {
            'realized_capacity_minutes': float(realized_capacity),
            'static_capacity_minutes': float(static_capacity),
            'scheduled_charger_hours': float(scheduled_ch),
            'active_charger_hours': float(realized_capacity / 60),
        }

    def compute_summary(self, n_chargers: int,
                        measurement_start_min: float,
                        measurement_duration_min: float,
                        activation_schedule: Optional[np.ndarray] = None
                        ) -> dict:
        """
        Compute aggregate metrics from session records.

        measurement_duration_min is the MEASUREMENT WINDOW.
        Sessions that arrived before the cutoff but completed after it
        are included in wait/throughput stats, but their busy time is
        clipped to the measurement window for utilization calculation.
        """
        measurement_end_min = measurement_start_min + measurement_duration_min
        self.sim_duration_min = measurement_duration_min

        cap_metrics = self._compute_capacity_metrics(
            n_chargers, measurement_start_min, measurement_duration_min,
            activation_schedule)

        _EMPTY = {
            'n_sessions': 0,
            'n_faults': 0,
            'n_faulted_customers': 0,
            'n_failed_after_retries': 0,
            'n_attempts': 0,
            'fault_fraction': 0.0,
            'fault_attempt_fraction': 0.0,
            'mean_wait': 0.0,
            'median_wait': 0.0,
            'p95_wait': 0.0,
            'p_wait_gt_10min': 0.0,
            'p_wait_gt_15min': 0.0,
            'mean_utilization': 0.0,
            'mean_utilization_static': 0.0,
            'throughput_per_hour': 0.0,
            'mean_service_duration': 0.0,
            'n_drained_after_horizon': 0,
        }
        _EMPTY.update(cap_metrics)

        if not self.sessions:
            return dict(_EMPTY)

        in_window = [s for s in self.sessions
                     if measurement_start_min <= s.arrival_time
                     < measurement_end_min]
        if not in_window:
            return dict(_EMPTY)

        waits = np.array([s.wait_time for s in in_window])
        faulted_customers = np.array([s.is_fault for s in in_window])
        total_faults = int(sum(s.n_faults for s in in_window))
        total_attempts = int(sum(s.n_attempts for s in in_window))
        successful = [s for s in in_window if s.completed_successfully]

        clipped_busy = 0.0
        for start, end in self.busy_intervals:
            clipped_busy += max(
                0.0,
                min(end, measurement_end_min)
                - max(start, measurement_start_min),
            )

        static_cap = cap_metrics['static_capacity_minutes']
        realized_cap = cap_metrics['realized_capacity_minutes']

        result = {
            'n_sessions': len(in_window),
            'n_faults': total_faults,
            'n_faulted_customers': int(faulted_customers.sum()),
            'n_failed_after_retries': int(len(in_window) - len(successful)),
            'n_attempts': total_attempts,
            'fault_fraction': float(faulted_customers.mean()),
            'fault_attempt_fraction': float(total_faults / total_attempts)
                                      if total_attempts else 0.0,
            'mean_wait': float(waits.mean()),
            'median_wait': float(np.median(waits)),
            'p95_wait': float(np.percentile(waits, 95)) if len(waits) > 0 else 0,
            'p_wait_gt_10min': float((waits > 10).mean()),
            'p_wait_gt_15min': float((waits > 15).mean()),
            'mean_utilization': float(clipped_busy / realized_cap)
                                if realized_cap > 0 else 0,
            'mean_utilization_static': float(clipped_busy / static_cap)
                                      if static_cap > 0 else 0,
            'throughput_per_hour': float(len(successful) /
                                        (measurement_duration_min / 60))
                                  if measurement_duration_min > 0 else 0,
            'mean_service_duration': float(
                np.mean([s.service_duration for s in in_window])),
            'n_drained_after_horizon': sum(
                1 for s in in_window
                if s.departure_time > measurement_end_min),
        }
        result.update(cap_metrics)
        return result


