def simulate_station(config: StationConfig,
                     n_replications: int = 50,
                     verbose: bool = False) -> List[dict]:
    """
    Run discrete-event simulation for one station configuration.

    Returns a list of summary dicts, one per replication.

    Activation mechanism:
    When config.activation_schedule is set, a simpy.Container is used
    as a token semaphore instead of simpy.Resource. Each token represents
    one available charger slot. Customers do container.get(1) to acquire
    a slot and container.put(1) to release it.

    Capacity changes at hour boundaries:
      - Increase from 3 to 5: any pending retirement debt from a
        previous downshift is canceled first (up to the increase
        amount). Remaining new tokens are added via container.put(),
        which immediately wakes waiting customers.
      - Decrease from 5 to 3: the scheduled capacity is updated and
        logged immediately. Idle excess tokens are retired right away
        via container.get(). If some tokens are still busy, the
        remainder is tracked as pending_retirements; when those
        customers finish service, the token is retired (not returned
        to the pool) instead of being put back.

    When activation_schedule is None, the original simpy.Resource is
    used for full backward compatibility.
    """
    if not HAS_SIMPY:
        raise ImportError("SimPy is required. Install with: pip install simpy")

    results = []
    measurement_duration_min = config.sim_days * 24 * 60
    measurement_start_min = config.warmup_days * 24 * 60
    sim_duration_min = measurement_start_min + measurement_duration_min

    use_activation = config.activation_schedule is not None
    clamped_schedule = None
    if use_activation:
        raw = np.asarray(config.activation_schedule, dtype=float)
        if raw.shape != (24,):
            raise ValueError(
                f"activation_schedule must be length 24, got shape {raw.shape}")
        if not np.all(np.isfinite(raw)):
            raise ValueError(
                f"activation_schedule contains non-finite values: "
                f"{raw[~np.isfinite(raw)]}")
        if np.any(raw < 0):
            raise ValueError(
                f"activation_schedule contains negative values: "
                f"{raw[raw < 0]}")
        clamped_schedule = np.clip(
            np.rint(raw).astype(int), 1, config.n_chargers)

    for rep in range(n_replications):
        base_seed = 42 if config.random_seed is None else config.random_seed
        seed = base_seed + rep
        # Separate streams prevent policy-dependent event ordering from
        # changing exogenous demand or customer attributes. Customer tapes
        # are generated at arrival and are therefore identical across fleet
        # sizes and scheduling policies for a given base seed/replication.
        seed_sequence = np.random.SeedSequence([base_seed, rep])
        arrival_ss, type_ss, service_ss, fault_ss, repair_ss = \
            seed_sequence.spawn(5)
        arrival_rng = np.random.default_rng(arrival_ss)
        type_rng = np.random.default_rng(type_ss)
        service_rng = np.random.default_rng(service_ss)
        fault_rng = np.random.default_rng(fault_ss)
        repair_rng = np.random.default_rng(repair_ss)
        env = simpy.Environment()

        metrics = MetricsCollector()
        session_counter = [0]

        if use_activation:
            initial_cap = int(clamped_schedule[0])
            slot_pool = simpy.Container(env, capacity=config.n_chargers,
                                        init=initial_cap)
            current_scheduled_cap = [initial_cap]
            n_busy = [0]
            chargers = None
        else:
            slot_pool = None
            chargers = simpy.Resource(env, capacity=config.n_chargers)

        if use_activation:
            metrics.log_capacity_event(0.0, initial_cap, 0)
        else:
            metrics.log_capacity_event(0.0, config.n_chargers, 0)

        pending_retirements = [0]

        def capacity_manager(env, slot_pool, clamped_schedule,
                             sim_duration_min, metrics,
                             current_scheduled_cap, n_busy, n_chargers,
                             pending_retirements):
            """Adjust available charger slots at each hour boundary."""
            while env.now < sim_duration_min:
                current_min = env.now
                next_hour_boundary = (int(current_min / 60) + 1) * 60.0
                yield env.timeout(next_hour_boundary - current_min)

                if env.now >= sim_duration_min:
                    break

                current_hour = int((env.now / 60) % 24)
                new_cap = int(clamped_schedule[current_hour])
                old_cap = current_scheduled_cap[0]

                if new_cap != old_cap:
                    current_scheduled_cap[0] = new_cap

                    delta = new_cap - old_cap
                    if delta > 0:
                        cancel = min(delta, pending_retirements[0])
                        pending_retirements[0] -= cancel
                        actual_new_tokens = delta - cancel
                        if actual_new_tokens > 0:
                            yield slot_pool.put(actual_new_tokens)
                    elif delta < 0:
                        to_retire = -delta
                        idle_tokens = int(slot_pool.level)
                        immediate = min(to_retire, idle_tokens)
                        if immediate > 0:
                            yield slot_pool.get(immediate)
                            to_retire -= immediate
                        pending_retirements[0] += to_retire

                metrics.log_capacity_event(
                    env.now, current_scheduled_cap[0], n_busy[0])

        def make_customer_record(tape, total_wait, n_attempts, n_faults,
                                 fault_downtime, service_start,
                                 completed_successfully):
            return SessionRecord(
                session_id=tape['session_id'],
                arrival_time=tape['arrival_time'],
                service_start_time=service_start,
                wait_time=total_wait,
                service_duration=(tape['service_duration']
                                  if completed_successfully else 0.0),
                departure_time=env.now,
                charger_type=tape['charger_type'],
                is_fault=n_faults > 0,
                n_attempts=n_attempts,
                n_faults=n_faults,
                fault_downtime=fault_downtime,
                completed_successfully=completed_successfully,
                total_system_time=env.now - tape['arrival_time'],
            )

        def customer_process_container(env, tape, metrics, slot_pool,
                                       n_busy, current_scheduled_cap,
                                       pending_retirements):
            """Serve one customer across all attempts without changing ID."""
            total_wait = 0.0
            n_faults = 0
            fault_downtime = 0.0
            last_release = tape['arrival_time']
            service_start = env.now

            for attempt_idx, faulted in enumerate(tape['fault_flags']):
                queue_enter = last_release
                yield slot_pool.get(1)
                attempt_start = env.now
                total_wait += attempt_start - queue_enter
                n_busy[0] += 1
                metrics.log_capacity_event(
                    env.now, current_scheduled_cap[0], n_busy[0])

                duration = (tape['repair_durations'][attempt_idx]
                            if faulted else tape['service_duration'])
                yield env.timeout(duration)
                metrics.add_busy_interval(attempt_start, env.now)

                n_busy[0] -= 1
                metrics.log_capacity_event(
                    env.now, current_scheduled_cap[0], n_busy[0])
                if pending_retirements[0] > 0:
                    pending_retirements[0] -= 1
                else:
                    yield slot_pool.put(1)

                if not faulted:
                    service_start = attempt_start
                    metrics.add_session(make_customer_record(
                        tape, total_wait, attempt_idx + 1, n_faults,
                        fault_downtime, service_start, True))
                    return

                n_faults += 1
                fault_downtime += duration
                last_release = env.now

            metrics.add_session(make_customer_record(
                tape, total_wait, len(tape['fault_flags']), n_faults,
                fault_downtime, service_start, False))

        def customer_process_resource(env, tape, metrics, chargers):
            """Serve one customer across all attempts using simpy.Resource."""
            total_wait = 0.0
            n_faults = 0
            fault_downtime = 0.0
            last_release = tape['arrival_time']
            service_start = env.now

            for attempt_idx, faulted in enumerate(tape['fault_flags']):
                queue_enter = last_release
                with chargers.request() as req:
                    yield req
                    attempt_start = env.now
                    total_wait += attempt_start - queue_enter
                    metrics.log_capacity_event(
                        env.now, config.n_chargers, len(chargers.users))

                    duration = (tape['repair_durations'][attempt_idx]
                                if faulted else tape['service_duration'])
                    yield env.timeout(duration)
                    metrics.add_busy_interval(attempt_start, env.now)
                    metrics.log_capacity_event(
                        env.now, config.n_chargers,
                        len(chargers.users) - 1)

                if not faulted:
                    service_start = attempt_start
                    metrics.add_session(make_customer_record(
                        tape, total_wait, attempt_idx + 1, n_faults,
                        fault_downtime, service_start, True))
                    return

                n_faults += 1
                fault_downtime += duration
                last_release = env.now

            metrics.add_session(make_customer_record(
                tape, total_wait, len(tape['fault_flags']), n_faults,
                fault_downtime, service_start, False))

        def arrival_process(env, config, metrics, arrival_rng,
                            type_rng, service_rng, fault_rng, repair_rng,
                            session_counter):
            """Main arrival process dispatcher."""
            def spawn_customer(env, ctype):
                sid = session_counter[0]
                session_counter[0] += 1
                fault_rates = config.fault_rates or DEFAULT_FAULT_RATES
                max_attempts = max(1, int(config.max_fault_retries) + 1)
                raw_fault_flags = [
                    is_session_faulted(ctype, fault_rates, fault_rng)
                    for _ in range(max_attempts)
                ]
                fault_flags = (raw_fault_flags if config.faults_enabled
                               else [False] * max_attempts)
                tape = {
                    'session_id': sid,
                    'arrival_time': env.now,
                    'charger_type': ctype,
                    'service_duration': min(max(sample_service_time(
                        ctype, config.service_params or {}, service_rng),
                        0.5), 720),
                    'fault_flags': fault_flags,
                    'repair_durations': [fault_repair_duration(
                        repair_rng,
                        median_min=config.fault_repair_median_min)
                        for _ in range(max_attempts)],
                }
                if use_activation:
                    env.process(customer_process_container(
                        env, tape, metrics, slot_pool, n_busy,
                        current_scheduled_cap, pending_retirements))
                else:
                    env.process(customer_process_resource(
                        env, tape, metrics, chargers))

            if config.arrival_mode == 'nhpp' and config.nhpp_rates is not None:
                lambda_max = config.nhpp_rates.max()
                if lambda_max <= 0:
                    return
                while env.now < sim_duration_min:
                    iat = arrival_rng.exponential(1.0 / lambda_max) * 60
                    yield env.timeout(iat)
                    if env.now >= sim_duration_min:
                        break
                    current_hour = int((env.now / 60) % 24)
                    current_rate = config.nhpp_rates[current_hour]
                    if arrival_rng.random() < current_rate / lambda_max:
                        ctype = choose_charger_type(
                            config.charger_type_mix, type_rng)
                        spawn_customer(env, ctype)

            elif config.arrival_mode == 'nhpp_gamma_poisson' and \
                    config.nhpp_rates is not None:
                alpha = max(float(config.arrival_overdispersion_alpha), 0.0)
                for hour_idx in range(int(np.ceil(sim_duration_min / 60))):
                    hour_start = hour_idx * 60.0
                    lam = max(0.0, float(
                        config.nhpp_rates[hour_idx % 24]))
                    if alpha > 0:
                        multiplier = arrival_rng.gamma(
                            shape=1.0 / alpha, scale=alpha)
                    else:
                        multiplier = 1.0
                    n_arr = arrival_rng.poisson(lam * multiplier)
                    offsets = np.sort(arrival_rng.uniform(
                        0, 60, size=n_arr)) if n_arr else []
                    for offset in offsets:
                        t = hour_start + float(offset)
                        if t >= sim_duration_min:
                            break
                        if t > env.now:
                            yield env.timeout(t - env.now)
                        ctype = choose_charger_type(
                            config.charger_type_mix, type_rng)
                        spawn_customer(env, ctype)
                if env.now < sim_duration_min:
                    yield env.timeout(sim_duration_min - env.now)

            elif config.arrival_mode == 'poisson':
                mean_iat_min = 60.0 / max(config.constant_lambda, 0.01)
                while env.now < sim_duration_min:
                    iat = arrival_rng.exponential(mean_iat_min)
                    yield env.timeout(iat)
                    if env.now >= sim_duration_min:
                        break
                    ctype = choose_charger_type(
                        config.charger_type_mix, type_rng)
                    spawn_customer(env, ctype)

            elif config.arrival_mode == 'ml_replay' and \
                    config.ml_hourly_predictions is not None:
                preds = config.ml_hourly_predictions
                for hour_idx in range(len(preds)):
                    hour_start = hour_idx * 60
                    if hour_start >= sim_duration_min:
                        break
                    lam = max(0, preds[hour_idx])
                    n_arr = arrival_rng.poisson(lam)
                    if n_arr > 0:
                        offsets = np.sort(
                            arrival_rng.uniform(0, 60, size=n_arr))
                        for offset in offsets:
                            t = hour_start + offset
                            if t >= sim_duration_min:
                                break
                            if t > env.now:
                                yield env.timeout(t - env.now)
                            ctype = choose_charger_type(
                                config.charger_type_mix, type_rng)
                            spawn_customer(env, ctype)
                if env.now < sim_duration_min:
                    yield env.timeout(sim_duration_min - env.now)

            else:
                print(f"[WARN] Unknown arrival mode: {config.arrival_mode}")
                return

        # Run simulation:
        #   1. Arrivals stop at sim_duration_min (the arrival_process loops
        #      already break when env.now >= sim_duration_min).
        #   2. After arrivals stop, let all in-progress and queued sessions
        #      complete — do NOT truncate with env.run(until=...).
        #   3. Compute metrics using sim_duration_min as the measurement
        #      window for utilization (busy time is clipped to the window).
        env.process(arrival_process(
            env, config, metrics, arrival_rng, type_rng, service_rng,
            fault_rng, repair_rng, session_counter))
        if use_activation:
            env.process(capacity_manager(
                env, slot_pool, clamped_schedule, sim_duration_min,
                metrics, current_scheduled_cap, n_busy,
                config.n_chargers, pending_retirements))
        # Run WITHOUT a hard cutoff so queued/in-service customers drain.
        drain_limit = sim_duration_min + 2160
        env.run(until=drain_limit)

        n_arrived = session_counter[0]
        n_completed = len(metrics.sessions)
        n_unfinished = n_arrived - n_completed
        drain_cap_hit = n_unfinished > 0

        if drain_cap_hit and verbose:
            print(f"    [WARN] Rep {rep}: drain cap hit, "
                  f"{n_unfinished} sessions unfinished. "
                  f"Results may be biased.")

        summary = metrics.compute_summary(
            config.n_chargers, measurement_start_min,
            measurement_duration_min,
            activation_schedule=clamped_schedule)
        summary['replication'] = rep
        summary['seed'] = seed
        summary['warmup_days'] = config.warmup_days
        summary['simulation_days'] = config.sim_days
        summary['n_unfinished_at_termination'] = n_unfinished
        summary['drain_cap_hit'] = drain_cap_hit
        results.append(summary)

        if verbose and rep % 10 == 0:
            drain_note = ' [DRAIN CAP HIT]' if drain_cap_hit else ''
            print(f"    Rep {rep}: {summary['n_sessions']} sessions, "
                  f"util={summary['mean_utilization']:.3f}, "
                  f"P(W>15)={summary['p_wait_gt_15min']:.3f}, "
                  f"ACH={summary['active_charger_hours']:.1f}/sim"
                  f"{drain_note}")

    return results


